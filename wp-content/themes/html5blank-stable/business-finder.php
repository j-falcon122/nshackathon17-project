								<h4>Mentorships</h4>
								<ul>
									<li>SCORE (www.score.org) is probably the best-known organization providing free (and confidential) mentoring to small business owners via its national network of some 13,000 retired business executives, leaders and volunteers. SCORE’s volunteer mentors share their expertise through both in-person and online counseling (via email).</li>

									<li>Small Business Development Centers (SBDCs) are another great source of free or low-cost help and advice for current and would-be business owners of all types in all locations. There are over 1,200 SBDC locations nationwide. For help locating one, visit www.asbdc-us.org.</li>

									<li>Women’s Business Centers (WBCs) offer business training, counseling and other resources to help women start and grow successful businesses. To find your nearest WBC check the Office of Women’s Business Ownership at www.sba.gov.</li>

									<li>Minority Business Development Centers, part of the U.S. Department of Commerce, offer free help to minority-owned businesses through about 40 centers nationwide.  Visit the Minority Business Development Agency at www.mbda.gov.</li>

									<li>Trade or Professional Associations: Many trade and professional associations operate mentoring programs for business owners just starting out. Some offer formal one-on-one mentoring sessions as well as group networking opportunities.  Check associations in your industry.</li>

									<li>Mentors for Government Contracting: If your business plans to sell to the federal government, the General Services Administration (GSA) offers a Mentor/Protégé Program designed to encourage prime contractors to help small businesses be more successful in government contracting and enhance their ability to perform successfully on government contracts and subcontracts. You’ll find it at GSA.gov.</li>

									<li>Your Own Network: Who do you know? Is there a previous boss who was very inspiring to you or a friend who is a business owner? Ask that person to be your mentor or share his or her successes and struggles. You have nothing to lose. Just be prepared to share with them why you chose them in particular, your goals and what you are looking for from them. The best way to connect these days is LinkedIn. Make sure you’re on it!</li>
								</ul>

							<div class="category">
								<h4>Outreach</h4>
									<p> Outreach is something that a small business owner can do to help their business grow</p>
								<h5>Work with Schools</h5>
									<p>Approach a school and offer it something that their funding typically won’t cover. It’s a win-win for them, so they’ll rarely decline the offer. What’s in it for you? Schools encompass the entire community. Students connect you to families and families connect you to neighborhoods. At the local level, this is even more effective than social media marketing.</p>

								<h5>NGO's(non-governmental organizations)</h5>
									<p>Just like schools, NGOs are often short on funding and open to mutually beneficial partnerships. By offering them products or services in exchange for positive publicity, you’ll build a respected brand image and foster highly profitable relationships. Host a fundraising event for an NGO and you could get your brand’s name on their staff’s shirts and caps, your logo on their marketing materials, etc.</p>

								<h5>Team up with other businesses</h5>
									<p>Pool your efforts with other local businesses. Put on a free event for the public or table at an annual event that attracts high numbers. The more you collaborate with other business owners, the more you get your bran’s name out there. And you mind find business partners for other co-marketing efforts in the future too.</p>

