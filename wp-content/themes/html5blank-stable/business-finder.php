								<h4>Best Practices</h4>
								<p>What Is a Best Practice?
									A best practice is the process of finding and using ideas and strategies from outside your company and industry to improve performance in any given area. Big businesses have used best practice benchmarking over decades and realized billions in savings and revenues in all areas of business operations and sales. Small business can reap even greater rewards from best practices.</p>

								<h5>Benefits of Best Practices for Small Business</h5>:
								<ul>
									<li>Reduce Costs: Small companies often do not have the deep financial pockets of big business to "re-invent the wheel." By learning what other companies have successfully done, a small business can save money without testing new ideas.</li>
									<li>Avoid Mistakes: Solving business problems on your own can result in costly errors. Learning what others have done can keep your business in business.</li>
									<li>Find New Ideas: Adopting the "not-invented-here" attitude can spell disaster for small businesses. Learn to borrow the best from beyond your company.</li>
									<li>Improve Performance: When your business looks for best practices outside your business, a wonderful thing happens. You raise the bar of performance and set new standards of excellence to propel your company forward.</li>
								</ul>
